#include <stdio.h>

int main() {
    // Initialize length and breadth
    float length = 50.0;
    float breadth = 30.0;

    // Calculate area and perimeter
    float area = length * breadth;
    float perimeter = 2 * (length + breadth);

    // Display results
    printf("For a rectangular plot with:\n");
    printf("Length = %.2f meters\n", length);
    printf("Breadth = %.2f meters\n", breadth);
    printf("Area = %.2f square meters\n", area);
    printf("Perimeter = %.2f meters\n", perimeter);

    printf("\n24CE008_Ansh");


    return 0;
}

